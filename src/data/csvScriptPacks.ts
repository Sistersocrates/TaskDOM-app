// Get random CSV script by criteria
export const getRandomCSVScript = (
  category?: string,
  subCategory?: string,
  nsfwLevel?: 'SFW' | 'NSFW'
): CSVScript | null => {
  let filtered = csvScripts;
  
  if (category) {
    filtered = filtered.filter(script => 
      script.category.toLowerCase().includes(category.toLowerCase())
    );
  }
  
  if (subCategory) {
    filtered = filtered.filter(script => 
      script.subCategory.toLowerCase().includes(subCategory.toLowerCase())
    );
  }
  
  if (nsfwLevel) {
    filtered = filtered.filter(script => script.nsfw_level === nsfwLevel);
  }
  
  if (filtered.length === 0) return null;
  
  return filtered[Math.floor(Math.random() * filtered.length)];
};

// Map voice styles to CSV script categories
const getCSVScriptsForVoiceStyle = (
  voiceStyle: 'flirty' | 'dominant' | 'wholesome',
  nsfwLevel: 'SFW' | 'NSFW' = 'SFW'
): CSVScript[] => {
  const categoryMap = {
    flirty: ['Flirty & Fun Praise'],
    dominant: ['Dominant & Dirty Praise'],
    wholesome: ['Flirty & Fun Praise'] // Use wholesome subcategories from Flirty & Fun
  };
  
  const relevantCategories = categoryMap[voiceStyle] || [];
  
  let scripts = csvScripts.filter(script => 
    relevantCategories.some(category => script.category.includes(category)) &&
    script.nsfw_level === nsfwLevel
  );

  // For wholesome, filter to only wholesome subcategories
  if (voiceStyle === 'wholesome') {
    scripts = scripts.filter(script => 
      script.subCategory.includes('Wholesome') || 
      script.vibe.includes('Gentler')
    );
  }

  return scripts;
};

